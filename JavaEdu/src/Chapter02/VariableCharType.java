package Chapter02;

public class VariableCharType {

	public static void main(String[] args) {
		// 문자 데이터 저장 타입 : char
		char ch1 = 'A';
		char ch2 = '자';
		char ch3 = 66;
		char ch4 = 51088;

		System.out.println(ch1);
		System.out.println(ch2);
		System.out.println(ch3);
		System.out.println(ch4);
	}

}
